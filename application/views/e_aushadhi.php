              <h2>e-Aushadhi</h2>
    <p>The corporation has decided that all the procurement should be made through online system only. 
        The Corporation has adopted the e-Aushadhi Software from the CDAC for the process of online indenting, e-tenders, online procurement, 
        distribution, Accountancy etc., from peripheral to state level.</p>
    <p>The e-Aushadhi project, which deals with the Drugs and Surgicals, 
        has been live at all the CMS across all the District HQs in the State since June 2015.</p>
    <p>In the first phase it is being  implemented in all hospitals under the control of DME & TSVVP.  
        It is proposed to extend the same to all the health facilities covered under the Director Health, 
        in the next 2-3 months.</p>
